package atourofgo

import "fmt"

// Vertexas — пример структуры (структурного типа)
// Структура — это набор связанных полей (как "карточка" с данными)
type Vertexas struct {
	X, Y int
}

// var блок — удобный способ объявить сразу несколько переменных
var (
	// 1. Позиционный литерал — значения идут строго по порядку полей
	v1 = Vertexas{1, 2} // X=1, Y=2

	// 2. Именованный литерал — указываем поля по имени
	// Это предпочтительный способ в Go — более читаемо
	v2 = Vertexas{X: 1} // Y:0 is implicit (неуказанные поля получают zero value)

	// 3. Пустой литерал — все поля получают нулевые значения
	v3 = Vertexas{} // X:0, Y:0

	// 4. Создаём структуру и сразу берём указатель на неё
	p = &Vertexas{1, 2} // имеет тип *Vertexas (указатель)
)

// struct_literals демонстрирует разные способы создания структур
func struct_literals() {
	fmt.Println(v1, p, v2, v3)
}
