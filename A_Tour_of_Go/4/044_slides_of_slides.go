package atourofgo

import (
	"fmt"
	"strings"
)

func slides_of_slides() {
	// Создаём двумерный слайс (3x3) — поле для крестиков-ноликов
	board := [][]string{
		{"_", "_", "_"},
		{"_", "_", "_"},
		{"_", "_", "_"},
	}

	// Игроки делают ходы
	board[0][0] = "X" // X в левый верхний угол
	board[2][2] = "O" // O в правый нижний угол
	board[1][2] = "X" // X в середину правого столбца
	board[1][0] = "O" // O в середину левого столбца
	board[0][2] = "X" // X в правый верхний угол

	// Выводим поле
	for i := 0; i < len(board); i++ {
		// strings.Join соединяет элементы слайса через пробел
		fmt.Printf("%s\n", strings.Join(board[i], " "))
	}
}
